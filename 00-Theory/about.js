```javascript
    알고리즘은 컴퓨터를 이용해 문제를 해결하기 위한 처리 절차입니다.
    입력->처리->출력 이라는 컴퓨터 프로그래밍을 이용한 문제해결 절차라고 할 수 있고, 컴퓨터의 데이터 처리 뿐만 아니라
    현실 세계의 다양한 과제 해결에 알고리즘을 활용할 수 있습니다.

    알고리즘은 음식 요리법과 같습니다. 문제를 해결하기 위한 과정이 마치 요리법처럼 절차와 방법을 준수하면 누구나 원하는
    요리를 만들듯이 문제를 해결할 수 있게 됩니다.

    좋은 알고리즘은 우수한 프로그램을 작성하기 위한 이정표와 같습니다.

    ex> 게임 잘 하는 방법 --> 공략법 === 알고리즘
```